
frase = input('Digite uma frase qualquer: ').upper().strip()

print('A letra "a" aparece {} vezes na frase acima'.format(frase.count('A')))

print('Na posição {} a letra "a" aparece pela primeira vez'.format(frase.find('A')+1))

print('Na posição {} a letra "a" aparece pela ultima vez'.format(frase.rfind('A')+1))







