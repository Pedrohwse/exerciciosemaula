'''ANSI Cores'''

print('\033[31m Olá Mundo\033[m') #Vermelho

print('\033[31;43mOlá mundo!\033[m') #Fundo amarelho, letra vermelha

print('\033[1;31;43mOlá mundo!\033[m') #Fundo amarelho, letra vermelha, em negrito

print('\033[1;37;41mOlá Mundo!\033[m') #Fundo vermelho letra branca

print('\033[1;35;43mOlá Mundo!\033[m') #Fundo amarelho letra roxa

print('\033[4;37;42mOlá Mundo!\033[m') #Fundo verde letra branca sublinhado

print('\033[7mOlá Mundo!\033[m') #Fundo branco letra preta








